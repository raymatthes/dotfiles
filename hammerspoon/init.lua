-- center and resize

hs.hotkey.bind({"cmd", "alt", "ctrl"}, "C", function()
  local win = hs.window.focusedWindow()
  local f = win:frame()
  local screen = win:screen()
  local max = screen:frame()


  local size = hs.geometry.size(max.w / 2, max.h / 2)
  win:setSize(size)
  win:centerOnScreen(screen)

end)


